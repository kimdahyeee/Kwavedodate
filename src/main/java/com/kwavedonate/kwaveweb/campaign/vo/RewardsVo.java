package com.kwavedonate.kwaveweb.campaign.vo;

public class RewardsVo {

	private int rewardNum;
	private String campaignName;
	private String rewardAmount;
	private String rewardTotalCnt;
	private String rewardCurrentCnt;
	private String rewardSubject;
	private String rewardContents;
	private String rewardImg;
	
	public int getRewardNum() {
		return rewardNum;
	}
	public void setRewardNum(int rewardNum) {
		this.rewardNum = rewardNum;
	}
	public String getCampaignName() {
		return campaignName;
	}
	public void setCampaignName(String campaignName) {
		this.campaignName = campaignName;
	}
	public String getRewardAmount() {
		return rewardAmount;
	}
	public void setRewardAmount(String rewardAmount) {
		this.rewardAmount = rewardAmount;
	}
	public String getRewardTotalCnt() {
		return rewardTotalCnt;
	}
	public void setRewardTotalCnt(String rewardTotalCnt) {
		this.rewardTotalCnt = rewardTotalCnt;
	}
	public String getRewardCurrentCnt() {
		return rewardCurrentCnt;
	}
	public void setRewardCurrentCnt(String rewardCurrentCnt) {
		this.rewardCurrentCnt = rewardCurrentCnt;
	}
	public String getRewardSubject() {
		return rewardSubject;
	}
	public void setRewardSubject(String rewardSubject) {
		this.rewardSubject = rewardSubject;
	}
	public String getRewardContents() {
		return rewardContents;
	}
	public void setRewardContents(String rewardContents) {
		this.rewardContents = rewardContents;
	}
	public String getRewardImg() {
		return rewardImg;
	}
	public void setRewardImg(String rewardImg) {
		this.rewardImg = rewardImg;
	}
	
}
